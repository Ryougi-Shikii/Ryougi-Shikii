#include<stdio.h>
int main(){
    int i,j,k;
    int a[3][3][3]=
    {
        {
            {1,2,3},     //a[0][0][0]  a[0][0][1]  a[0][0][2]
            {4,5,6},     //a[0][1][0]  a[0][1][1]  a[0][1][2]
            {7,8,9}      //a[0][2][0]  a[0][2][1]  a[0][2][2]
        },
        {   
            {11,12,13},             //o=a[0]
            {14,15,16},   //i=a[1]  //o=a[1]
            {17,18,19}
        },
        {   
            {21,22,23},             //o=a[0]
            {24,25,26},   //i=a[2]  //o=a[1]
            {27,28,29}              //o=a[2]
        }
    };
    for (i=0; i<3; i++){
        for (j=0; j<3; j++){
            for (k=0; k<3; k++){
                printf("%d ", a[j][i][k]);
            }
            printf("\n");
        }
        printf("\n");
    }
    return 0;
}
